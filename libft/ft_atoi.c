#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int		sign;
	int		num;
	size_t	i;

	i = 0;
	sign = 1;
	num = 0;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
	{
		i++;
	}
	if ((nptr[i] == '-' || nptr[i] == '+'))
	{
		if ((nptr[i + 1] == '-' || nptr[i + 1] == '+'))
			return (0);
		if (nptr[i] == '-')
			sign *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		num = num * 10 + (nptr[i] - '0');
		i++;
	}
	return (num * sign);
}
